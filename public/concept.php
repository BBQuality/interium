     <h2 class="mb-2 text-uppercase pt-5 fw-bold" style="color: #9e1e10;">Концепція Інтермаріуму</h2>
     <hr>
     <div class="row m-3 p-3 justify-content-center ">
    
    <div class="col-10">
    <div class="card  mb-3" style="max-width: 1624px;">
      <div class="row g-0">
        <div class="col-md-4">
         <img src="/img/inmarmap.webp" class="float-start top-50 border-end w-100 px-5 py-5 my-5">
